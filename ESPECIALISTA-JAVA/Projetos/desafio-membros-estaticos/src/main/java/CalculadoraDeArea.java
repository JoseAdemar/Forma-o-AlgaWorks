public class CalculadoraDeArea {

    static double calcularAreaDeUmQuadrado(double lado){
        double areaDoQuadrado = (lado * lado);
        return areaDoQuadrado;
    }

    static double calcularAreaDeUmCirculo(double raio){
        double areaDoCirculo = (raio * raio) * 3.14;
        return  areaDoCirculo;
    }
}
